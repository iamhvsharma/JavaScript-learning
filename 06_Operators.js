let a = 20;
let b = 10;

console.log("a+b =", a+b); // Add Operator 
console.log("a-b =", a-b); // Subtract Operator 
console.log("a*b =", a*b); // Multiply Operator 
console.log("a/b =", a/b); // Divide Operator 
console.log("a**b =", a**b); // Exponential Operator (Power)
console.log("a++ =", a++); // Increment Operator 
console.log("a-- =", a--); // Decrement Operator

// Assignment Operators 

let a1 = 1; // x = y

a1 += 5; // x = x+y
console.log("a1 is now = ", a1);

a1 -= 5; // x = x-y
console.log("a1 is now = ", a1);

a1 *= 5; // x = x*y
console.log("a1 is now = ", a1);

a1 /= 5; // x = x / y
console.log("a1 is now = ", a1);

a1 %= 5; // x = x%y
console.log("a1 is now = ", a1); 

a1 **= 5; // x = x**y
console.log("a1 is now = ", a1);


// Comparision Operstors

let comp1 = 6;
let comp2 = 7

console.log("comp1 == comp2", comp1 == comp2); // Checks Value only
console.log("comp1 != comp2", comp1 != comp2);
console.log("comp1 === comp2", comp1 === comp2); // Checks value and datatype both
console.log("comp1 !== comp2", comp1 !== comp2);


